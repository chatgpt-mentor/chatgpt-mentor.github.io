const availableLanguages = {
    "de": "Deutsch",
    "en": "English"
};

function setLanguage(lang) {
    const translation = translations[lang];
    if (!translation) return;

    // Update HTML lang attribute
    document.documentElement.lang = lang;

    // Update meta tags
    document.title = translation.page_title;
    document.querySelector('meta[name="description"]').setAttribute('content', translation.meta_description);

    // Update all elements with data-i18n attribute
    document.querySelectorAll('[data-i18n]').forEach(element => {
        const key = element.getAttribute('data-i18n');
        if (translation[key]) {
            element.textContent = translation[key];
        }
    });

    // Store selected language in local storage
    localStorage.setItem('lang', lang);
}

function loadLanguage() {
    const urlParams = new URLSearchParams(window.location.search);
    let lang = urlParams.get('lang') || localStorage.getItem('lang') || 'de';

    if (!translations[lang]) {
        lang = 'de';
    }

    setLanguage(lang);
    updateLanguageSwitcher(lang);
}

function updateLanguageSwitcher(currentLang) {
    const switcher = document.getElementById('language-switcher');
    if (switcher) {
        switcher.innerHTML = '';
        for (const code in availableLanguages) {
            const name = availableLanguages[code];
            const li = document.createElement('li');
            li.innerHTML = `<a href="?lang=${code}" class="lang-link ${code === currentLang ? 'active' : ''}">${name}</a>`;
            switcher.appendChild(li);
        }
    }
}

document.addEventListener('DOMContentLoaded', loadLanguage);
